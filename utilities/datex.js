if (!Date.prototype.toFilename) {
  (function() {

    function pad(number) {
      if (number < 10) {
        return '0' + number;
      }
      return number;
    }

    Date.prototype.toFilename = function() {
      return this.getUTCFullYear() +
        '-' + pad(this.getUTCMonth() + 1) +
        '-' + pad(this.getUTCDate()) +
        'T' + pad(this.getUTCHours()) +
        '-' + pad(this.getUTCMinutes()) +
        '-' + pad(this.getUTCSeconds());
    };

  }());
}